    private void abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abrirActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser=new JFileChooser();
        FileFilter filter = new FileNameExtensionFilter("txt");
        chooser.setFileFilter(filter);
        int result=chooser.showOpenDialog(null);
        if(result== JFileChooser.APPROVE_OPTION){
            this.archivoAnalizar.setearDatos(chooser);
            this.nombreArchivo.setText(this.archivoAnalizar.getNombre());
            this.entrada.setText(this.archivoAnalizar.getInfo());
        }
  
    }//GEN-LAST:event_abrirActionPerformed

    private void btnCompilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompilaActionPerformed
        // TODO add your handling code here:
        if(this.archivoAnalizar.getDocumento()!=null){
            this.archivoAnalizar.limpiar();
            this.archivoAnalizar.analizadorTokens();
            this.archivoAnalizar.escribirArchivo();
            this.archivoAnalizar.analisisSintax();
            this.resultado.setText(this.archivoAnalizar.getConsola());
        }else{
            this.resultado.setText("Por favor selecciona alg√∫n .");
        }
    }//GEN-LAST:event_btnCompilaActionPerformed

